def execute_intersect(conn, active_filters):
    """Run filters"""
    if not active_filters: return None
    full_sqls = []
    full_params = []
    for key, (sql, params) in active_filters.items():
        full_sqls.append(sql)
        full_params.extend(params)

    final_sql = " \nINTERSECT\n ".join(full_sqls)# Intersection
    try:
        cur = conn.cursor()
        cur.execute(final_sql, full_params)
        return set(row[0] for row in cur.fetchall())
    except sqlite3.Error as e:
        print(f"SQL Execution Error: {e}")
        return set()


