def show_visual_chart(results):
    """
    Suppress warnings + Display ID + Colab adaptation
    """
    if not results:
        print("❌ No data to visualize.")
        return

    # --- 1. Forcefully suppress all font warnings (Critical Step) ---
    # This line makes matplotlib silent
    logging.getLogger('matplotlib.font_manager').setLevel(logging.ERROR)

    # --- 2. Reset plotting parameters ---
    # Clear previously set 'SimHei' or 'Microsoft YaHei', restore default English
    plt.rcdefaults()

    # Create DataFrame
    df = pd.DataFrame(results)
    df_sorted = df.sort_values('PositiveRate', ascending=True)

    # Generate IDs
    df_sorted['DisplayID'] = [str(i) for i in range(1, len(df_sorted) + 1)]

    # Setup Canvas
    plt.figure(figsize=(12, 7))

    # Color Mapping
    norm = mcolors.Normalize(vmin=40, vmax=100)
    cmap = plt.get_cmap('RdYlGn')
    colors = cmap(norm(df_sorted['PositiveRate'].values))

    # Draw
    bars = plt.barh(df_sorted['DisplayID'], df_sorted['PositiveRate'], color=colors, alpha=0.8, edgecolor='black')

    # Labels
    plt.title('Game Positive Ratings (By ID)', fontsize=15, pad=15)
    plt.xlabel('Positive Rating (%)', fontsize=12)
    plt.ylabel('Game ID', fontsize=12)
    plt.xlim(0, 115)
    plt.grid(axis='x', linestyle='--', alpha=0.3)

    # Add text
    for bar in bars:
        width = bar.get_width()
        plt.text(width + 1, bar.get_y() + bar.get_height() / 2,
                 f'{int(width)}%', va='center', fontsize=10, fontweight='bold')

    plt.tight_layout()

    # --- 3. Print Legend ---
    print("\n" + "="*40)
    print("📋 CHART LEGEND ")
    print("="*40)
    print(f"{'ID':<5} | {'Game Name'}")
    print("-" * 40)
    for index, row in df_sorted.iloc[::-1].iterrows():
        print(f"{row['DisplayID']:<5} | {row['Name']}")
    print("="*40 + "\n")

    # --- 4. Since it's Colab, no need to close window ---
    print("✅ Chart generated below.")
    plt.show()
    print("\n⬇️ The program is still running... Please scroll down to see the subsequent input boxes. ⬇️")

