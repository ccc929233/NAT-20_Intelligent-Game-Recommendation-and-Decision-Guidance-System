def generate_all_reports_for_game(game_name: str, db_path: str, select_templates: List[str]):
    """
    Generate all selected reports for the specified game, templates dynamically loaded from the database.
    """
    # 1. Get all templates
    all_templates = get_all_templates(db_path)
    if not all_templates:
        print("Error: Cannot load report templates from database, program terminated.")
        return

    # 2. Get data for the specified game
    game_data = get_game_data(db_path, game_name)
    if not game_data:
        return

    # 3. Map report names to their generator functions
    report_functions = {
        'developer_card': generate_developer_card,
        'type_positioning': generate_type_positioning,
        'recent_popularity': generate_recent_popularity,
        'multi_platform_fit': generate_multi_platform_fit,
        'value_king': generate_value_king,
        'comprehensive_recommendation': generate_comprehensive_recommendation,
    }

    print(f"==================== 《{game_name}》report ====================\\n")

    # 4. Iterate through selected templates, generate and print reports
    for template_name in select_templates:
        report_func = report_functions.get(template_name)
        template_text = all_templates.get(template_name)

        if report_func and template_text:
            report = report_func(game_data, template_text)
            print(report)
        else:
            print(f"--- error：can,t find '{template_name}' ---\\n")

    print("============================== end ==============================")

