def enter_sub_menu(conn, menu_name, questions, active_filters):
    """Enter sub-menu configuration (With 0-result Undo)"""
    print(f"\n--- Configuring {menu_name} ---")

    # 1. Calculate base count on entry
    base_ids = execute_intersect(conn, active_filters)
    base_count = len(base_ids) if base_ids is not None else 0
    print(f"ℹ️ Current base match: {base_count} games")

    if base_count == 0 and len(active_filters) > 0:
        print(
            "⚠️ Warning: You already have 0 matches. Further filtering might be useless. Consider undoing previous choices.")

    # 2. Loop through questions
    for q_key, q_data in questions.items():
        print(f"\n>> {q_data['title']}")
        opt_list = [f"[{k}] {v}" for k, v in q_data['options'].items()]
        for i in range(0, len(opt_list), 2):
            print("   " + "  |  ".join(opt_list[i:i + 2]))

        # Show current status
        if q_key in active_filters:
            print(f"   [Selected: Active]")

        max_k = max(q_data['options'].keys()) if q_data['options'] else 0
        ans = get_user_input("Select (0 to keep/skip)", max_k)

        if ans != 0:
            # === A. Backup old state ===
            had_old_value = q_key in active_filters
            old_value = active_filters.get(q_key)

            # === B. Try apply new filter ===
            active_filters[q_key] = q_data['func'](ans)

            # === C. Calculate new count ===
            current_ids = execute_intersect(conn, active_filters)
            count = len(current_ids) if current_ids is not None else 0

            # === D. Judge and Remedy ===
            if count > 0:
                print(f"   ✅ Filter applied | 📊 Remaining matches: {count} games")
            else:
                print(f"   ⚠️  Oops! This choice resulted in 0 matches.")
                print("   [1] ↩️ Undo selection (Recommended)")
                print("   [2] 🔒 Keep it (I want this specific criteria)")

                fix_choice = input("   >> Select action (1/2): ")

                if fix_choice == '2':
                    print("   🆗 Empty result retained.")
                else:
                    # Execute Undo
                    print("   ↩️ Undoing...")
                    if had_old_value:
                        active_filters[q_key] = old_value  # Restore old value
                    else:
                        del active_filters[q_key]  # Delete if didn't exist

                    # Recalculate
                    restored_ids = execute_intersect(conn, active_filters)
                    r_count = len(restored_ids) if restored_ids is not None else 0
                    print(f"      📊 Restored matches: {r_count} games")

    input("\nPress Enter to return to Dashboard...")


